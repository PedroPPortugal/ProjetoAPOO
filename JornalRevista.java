public class JornalRevista {
    String titulo;
    String editora;
    String issn;
    String dataPublicacao;
    String categoria;

    public JornalRevista(String titulo, String editora, String issn, String dataPublicacao, String categoria) {
        this.titulo = titulo;
        this.editora = editora;
        this.issn = issn;
        this.dataPublicacao = dataPublicacao;
        this.categoria = categoria;
    }
}
